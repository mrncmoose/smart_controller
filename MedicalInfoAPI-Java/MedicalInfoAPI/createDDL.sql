ALTER TABLE MEDICALAREAOFUSE ADD CONSTRAINT FK_MEDICALAREAOFUSE_MEDAREA_MEDICALAREAID FOREIGN KEY (MEDAREA_MEDICALAREAID) REFERENCES MEDICALAREA (MEDICALAREAID);
ALTER TABLE MEDICALAREAOFUSE ADD CONSTRAINT FK_MEDICALAREAOFUSE_PATIENT_PATIENTID FOREIGN KEY (PATIENT_PATIENTID) REFERENCES PATIENT (PATIENTID);
ALTER TABLE MEDICALAREAOFUSE ADD CONSTRAINT MEDICALAREAOFUSE_REASONFORVISIT_REASONFORVISITID FOREIGN KEY (REASONFORVISIT_REASONFORVISITID) REFERENCES REASONFORVISIT (REASONFORVISITID);

