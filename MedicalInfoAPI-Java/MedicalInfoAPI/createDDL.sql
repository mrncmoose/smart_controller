use `moosewaredata`;

SET foreign_key_checks = 0;

drop table if exists PATIENT; 
CREATE TABLE PATIENT (PATIENTID INTEGER AUTO_INCREMENT NOT NULL, FAMILYNAME VARCHAR(255) NOT NULL, GIVENNAME VARCHAR(255) NOT NULL, EMRSYSTEM_EMRID INTEGER NOT NULL, PRIMARY KEY (PATIENTID))
ENGINE=INNODB;

drop table if exists EMRSYSTEM;
CREATE TABLE EMRSYSTEM (EMRID INTEGER AUTO_INCREMENT NOT NULL, BASEURL VARCHAR(255), EMRNAME VARCHAR(255), GETPATIENTALLERGYRESOURCE VARCHAR(255), GETPATIENTMEDICATIONSRESOURCE VARCHAR(255), GETPATIENTRESOURCE VARCHAR(255), PUTPATIENTALLERGYRESOURCE VARCHAR(255), PUTPATIENTMEDICATIONSRESOURCE VARCHAR(255), PUTPATIENTRESOURCE VARCHAR(255), PRIMARY KEY (EMRID))
ENGINE=INNODB;

drop table if exists MEDICALAREA;
CREATE TABLE MEDICALAREA (MEDICALAREAID INTEGER AUTO_INCREMENT NOT NULL, FLOOR VARCHAR(255) NOT NULL, LOCATION POLYGON not null, NAME VARCHAR(255) NOT NULL UNIQUE, SPATIAL INDEX(LOCATION), PRIMARY KEY (MEDICALAREAID))
ENGINE=INNODB;

drop table if exists MEDICALAREAOFUSE;
CREATE TABLE MEDICALAREAOFUSE (MEDICALAREAOFUSEID INTEGER AUTO_INCREMENT NOT NULL, MEDAREA_MEDICALAREAID INTEGER NOT NULL, PATIENT_PATIENTID INTEGER NOT NULL, REASONFORVISIT_REASONFORVISITID INTEGER NOT NULL, PRIMARY KEY (MEDICALAREAOFUSEID))
ENGINE=INNODB;

drop table if exists REASONFORVISIT;
CREATE TABLE REASONFORVISIT (REASONFORVISITID INTEGER AUTO_INCREMENT NOT NULL, REASONFORVISIT VARCHAR(255), PRIMARY KEY (REASONFORVISITID))
ENGINE=INNODB;

